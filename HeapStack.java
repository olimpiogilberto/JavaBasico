//Exemplo HeapStack
 class HeapStack 
 {  public static void main(String a[])
	{
		int num1=2, num2=2;
		Integer objeto1= new Integer(2);
		Integer objeto2= new Integer(2);
		//comparando tipos primitivos
		if(num1==num2)
		{
        System.out.println("num1 é igual num2");
		}else
		{	
		System.out.println("Num1 é diferente de Num2");
		}
		//Comparando tipos de referência instância
		if(objeto1==objeto2)
		{
		System.out.println("Objeto1 é igual o objeto2");
		}else
		{	
		System.out.println("Objeto1 é diferente do objeto2");
		}
		
	}

}
